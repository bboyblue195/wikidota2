package bean;

public class BieuMau {
	private String mabm;
	private String noidung;
	public BieuMau() {
		super();
	}
	public BieuMau(String mabm, String noidung) {
		super();
		this.mabm = mabm;
		this.noidung = noidung;
	}
	public String getMabm() {
		return mabm;
	}
	public void setMabm(String mabm) {
		this.mabm = mabm;
	}
	public String getNoidung() {
		return noidung;
	}
	public void setNoidung(String noidung) {
		this.noidung = noidung;
	}
	
}
